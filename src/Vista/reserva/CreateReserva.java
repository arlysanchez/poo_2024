    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String dni = txtDni.getText();
        
        IClientDao CDao = new ClienteDaoImpl();
        Cliente cliente = CDao.searchByDNI(dni);
        
        if(cliente == null){
            mostrarMensaje("Cliente no registrado:");
        }else{
            this.idCliente = cliente.getIdcliente();
            LabelDni.setText(cliente.getDni());
            LabelNombre.setText(cliente.getNombre());
            LabelEmail.setText(cliente.getEmail());
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

